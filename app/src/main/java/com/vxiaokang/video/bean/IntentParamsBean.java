package com.vxiaokang.video.bean;

/**
 * activity 参数传递
 */
public class IntentParamsBean {
    private String sourceId;
    private String fromType;
    private String sourceUrl;
    private String sourceLink;
    private String coverImage;
    private String sourceTitle;
    private String sourceViews;
    private String sourceDuration;
    private String sourceItemId;
    private String sourceItemName;
    private String sourcePosition;

    public String getFromType() {
        return fromType;
    }

    public void setFromType(String fromType) {
        this.fromType = fromType;
    }

    public String getSourceUrl() {
        return sourceUrl;
    }

    public void setSourceUrl(String sourceUrl) {
        this.sourceUrl = sourceUrl;
    }

    public String getCoverImage() {
        return coverImage;
    }

    public void setCoverImage(String coverImage) {
        this.coverImage = coverImage;
    }

    public String getSourceTitle() {
        return sourceTitle;
    }

    public void setSourceTitle(String sourceTitle) {
        this.sourceTitle = sourceTitle;
    }

    public String getSourceViews() {
        return sourceViews;
    }

    public void setSourceViews(String sourceViews) {
        this.sourceViews = sourceViews;
    }

    public String getSourceDuration() {
        return sourceDuration;
    }

    public void setSourceDuration(String sourceDuration) {
        this.sourceDuration = sourceDuration;
    }

    public String getSourceItemId() {
        return sourceItemId;
    }

    public void setSourceItemId(String sourceItemId) {
        this.sourceItemId = sourceItemId;
    }

    public String getSourceItemName() {
        return sourceItemName;
    }

    public void setSourceItemName(String sourceItemName) {
        this.sourceItemName = sourceItemName;
    }

    public String getSourceId() {
        return sourceId;
    }

    public void setSourceId(String sourceId) {
        this.sourceId = sourceId;
    }

    public String getSourcePosition() {
        return sourcePosition;
    }

    public void setSourcePosition(String sourcePosition) {
        this.sourcePosition = sourcePosition;
    }

    public String getSourceLink() {
        return sourceLink;
    }

    public void setSourceLink(String sourceLink) {
        this.sourceLink = sourceLink;
    }
}
